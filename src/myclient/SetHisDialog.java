    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        host = txtHost.getText().toString();
        user = txtUser.getText().toString();
        pass = txtPass.getText().toString();
        db = txtDb.getText().toString();


        try {
            Ini ini = new Ini(new File("config.ini"));
            ini.put("Connection", "host", host);
            ini.put("Connection", "user", user);
            ini.put("Connection", "pass", pass);
            ini.put("Connection", "database", db);
            ini.put("PCUCODE","pcucode",pcucode);
            ini.store();
        } catch (IOException ex) {
            ex.printStackTrace();
        }


        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        host = txtHost.getText().toString();
        user = txtUser.getText().toString();
        pass = txtPass.getText().toString();
        db = txtDb.getText().toString();
        if (db.equals("") || host.equals("")) {
            return;
        }
        SimpleMySQL mysql = new SimpleMySQL();

        if (mysql.connect(host, user, pass, db, "tis-620")) {

            try {
                ResultSet rs = mysql.query("select hospitalcode from opdconfig limit 1");
                rs.next();
                pcucode = rs.getString("hospitalcode");
            } catch (SQLException e) {
                e.printStackTrace();
            }
            lbTest.setForeground(Color.green);
            lbTest.setText("เชื่อมต่อสำเร็จ");
        } else {
            lbTest.setForeground(Color.red);
            lbTest.setText("ไม่สามารถเชื่อมต่อได้");
        }
        mysql.close();

    }//GEN-LAST:event_jButton3ActionPerformed

